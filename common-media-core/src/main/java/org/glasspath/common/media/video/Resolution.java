package org.glasspath.common.media.video;

public enum Resolution {

	VGA("VGA (640x480)", 640, 480),
	SVGA("SVGA (800x600)", 800, 600),
	HD_720P("HD 720p (1280x720)", 1280, 720),
	FHD_1080P("FHD 1080p (1920x1080)", 1920, 1080);

	private final String name;
	private final int width;
	private final int height;

	private Resolution(String name, int width, int height) {
		this.name = name;
		this.width = width;
		this.height = height;
	}

	public String getName() {
		return name;
	}

	public int getWidth() {
		return width;
	}

	public int getHeight() {
		return height;
	}

	@Override
	public String toString() {
		return name;
	}

}
